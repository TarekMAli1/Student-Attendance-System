    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        instructor_email=jTextField1.getText();
        String password=jPasswordField1.getText();
        if(checker.verify_email(instructor_email) && checker.verify_pass(password))
        {
            if(my_db.search_instructor(instructor_email, password)==true)
            {
                jLabel5.setText("Login Success!");
                this.dispose();
                new InstructorPanelGUI().setVisible(true);
            }
            else
            {
                jLabel5.setText("User Not Recognized!!");
            }

        }
        else
        {
            jLabel5.setText("Invaled Info!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

